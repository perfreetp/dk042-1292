function generateVitalRecords(patientId: string, weeks: number): VitalRecord[] {
  const records: VitalRecord[] = [];
  for (let i = 8; i <= weeks; i += 2) {
    const date = new Date();
    date.setDate(date.getDate() - (weeks - i) * 7);
    const systolic = 110 + Math.floor(Math.random() * 50);
    const diastolic = 70 + Math.floor(Math.random() * 30);
    records.push({
      id: `V${patientId}-${i}`,
      patientId,
      recordDate: date.toISOString().split('T')[0],
      gestationalWeeks: i,
      systolicBP: systolic,
      diastolicBP: diastolic,
      heartRate: 78 + Math.floor(Math.random() * 15),
      temperature: 36.4 + Math.random() * 0.4,
      weight: 52 + (i - 8) * 0.6 + Math.random() * 2,
      fundalHeight: i >= 12 ? Math.max(0, i - 12 + Math.random() * 3) : 0,
      abdominalCircumference: i >= 16 ? 70 + (i - 16) * 1.5 + Math.random() * 5 : 0,
      fetalHeartRate: i >= 12 ? 120 + Math.floor(Math.random() * 40) : 0,
      edema: Math.random() > 0.7 ? '轻度' : '无',
      proteinuria: Math.random() > 0.8 ? '+' : '阴性',
      symptoms: '',
      note: '',
    });
  }
  return records;
}

